def max_candies(candyType):
    max_types = len(set(candyType))  # Count the number of unique candy types
    max_allowed = len(candyType) // 2  # Calculate the maximum number of candies allowed to eat

    return min(max_types, max_allowed)


candyType = [1, 1, 2, 2, 3, 3]
result = max_candies(candyType)
print(result) 
